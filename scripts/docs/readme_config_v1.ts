import fs from "fs";
import path from "path";

export function generateReadMe(root: string) {
    if (!fs.existsSync(root)) {
        fs.mkdirSync(root, { recursive: true });
    }

    const readmeContent = `# ExpressKit Project

An opinionated Express.js backend generated by [ExpressKit](https://github.com/pd241008/ExpressKit). This project enforces a modular microservice architecture and strict Separation of Concerns (SoC) right out of the box.

## 🚀 Getting Started

### Prerequisites
- Node.js (v18 or higher recommended)
- npm or yarn

### Running the App
Since you generated this project using the CLI, all your dependencies are already installed.

To start the development server:
\`\`\`bash
npm run dev
\`\`\`

To build for production:
\`\`\`bash
npm run build
npm start
\`\`\`

## 🏗️ Architecture & Philosophy

ExpressKit is built on the principle of **Separation of Concerns**. The boilerplate strictly isolates:
*   **Routes:** API endpoint definitions.
*   **Controllers:** Request/Response handling logic.
*   **Services:** Core business logic and database interactions.
*   **Middlewares:** Request validation, authentication, and error handling.

This opinionated structure ensures that as your application scales into a complex ecosystem, the codebase remains maintainable and deeply organized.

## 📁 Directory Structure

*   \`src/routes/\`: Your API endpoints.
*   \`src/controllers/\`: Handle incoming HTTP requests and map them to services.
*   \`src/services/\`: Business logic and database interactions.
*   \`src/models/\`: Database schemas and interfaces.
*   \`src/middleware/\`: Custom Express middlewares.
*   \`src/utils/\`: Helper functions and utilities.
*   \`src/config/\`: Application configuration.

## 🛠️ Upcoming Features

ExpressKit is constantly evolving. In the future, you will be able to use code generators:
*   \`npx @pd241008/expresskit generate controller <name>\` (Coming soon)
*   \`npx @pd241008/expresskit generate service <name>\` (Coming soon)

## 📝 License

Distributed under the MIT License. See \`LICENSE\` for more information.

---
*Built with precision for scalable backends by Prathmesh P. Desai.*
`;

    // Write the standard markdown file to the specified root directory
    fs.writeFileSync(
        path.join(root, "README.md"),
        readmeContent,
        "utf8"
    );
}